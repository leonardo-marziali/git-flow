// Function to greet the user
function greet(name) {
    console.log(`Hello, ${name}!`);
}

// Function to calculate the cube of a number
function cube(num) {
    return num * num * num;
}

// Call the greet function
greet("World");

// Call the cube function
console.log("The cube of 3 is:", cube(3));
